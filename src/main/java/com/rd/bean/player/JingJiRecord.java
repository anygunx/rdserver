package com.rd.bean.player;

public class JingJiRecord {
    private int id;
    private byte ty;
    private long ti;
    private byte tzt;
    private byte re;
    private short ra;

    public short getRa() {
        return ra;
    }

    public void setRa(short ra) {
        this.ra = ra;
    }

    public byte getRe() {
        return re;
    }

    public void setRe(byte re) {
        this.re = re;
    }

    public byte getTy() {
        return ty;
    }

    public byte getTzt() {
        return tzt;
    }

    public void setTzt(byte tzt) {
        this.tzt = tzt;
    }

    public void setTy(byte ty) {
        this.ty = ty;
    }

    public long getTi() {
        return ti;
    }

    public void setTi(long ti) {
        this.ti = ti;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


}
