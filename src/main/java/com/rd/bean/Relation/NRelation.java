package com.rd.bean.Relation;

public class NRelation {
    /**
     * 接收友情币数量
     */
    private int rfc;
    /**
     * 接收友情币次数
     */
    private int rfcc;
    /**
     * 发送友情币次数
     */
    private int sfcc;

    public int getRfc() {
        return rfc;
    }

    public void setRfc(int rfc) {
        this.rfc = rfc;
    }

    public int getRfcc() {
        return rfcc;
    }

    public void setRfcc(int rfcc) {
        this.rfcc = rfcc;
    }

    public int getSfcc() {
        return sfcc;
    }

    public void setSfcc(int sfcc) {
        this.sfcc = sfcc;
    }

}
