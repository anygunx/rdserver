package com.rd.model.data;

public class GangData {

    private short level;

    private int exp;

    private short maxMember;

    private short turntableMax;

    private short skillMax;

    private short skill2Max;

    public short getLevel() {
        return level;
    }

    public void setLevel(short level) {
        this.level = level;
    }

    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }

    public short getMaxMember() {
        return maxMember;
    }

    public void setMaxMember(short maxMember) {
        this.maxMember = maxMember;
    }

    public short getTurntableMax() {
        return turntableMax;
    }

    public void setTurntableMax(short turntableMax) {
        this.turntableMax = turntableMax;
    }

    public short getSkillMax() {
        return skillMax;
    }

    public void setSkillMax(short skillMax) {
        this.skillMax = skillMax;
    }

    public short getSkill2Max() {
        return skill2Max;
    }

    public void setSkill2Max(short skill2Max) {
        this.skill2Max = skill2Max;
    }
}
