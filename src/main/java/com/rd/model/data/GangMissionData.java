package com.rd.model.data;

public class GangMissionData {

    private byte id;

    private byte eventType;

    private byte count;

    private byte gangExp;

    private byte gangDonate;

    public byte getId() {
        return id;
    }

    public void setId(byte id) {
        this.id = id;
    }

    public byte getEventType() {
        return eventType;
    }

    public void setEventType(byte eventType) {
        this.eventType = eventType;
    }

    public byte getCount() {
        return count;
    }

    public void setCount(byte count) {
        this.count = count;
    }

    public byte getGangExp() {
        return gangExp;
    }

    public void setGangExp(byte gangExp) {
        this.gangExp = gangExp;
    }

    public byte getGangDonate() {
        return gangDonate;
    }

    public void setGangDonate(byte gangDonate) {
        this.gangDonate = gangDonate;
    }
}
