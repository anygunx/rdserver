    public static final Map<Integer, EGoodsChangeType> valueMap = new HashMap<Integer, EGoodsChangeType>() {
        private static final long serialVersionUID = 1L;

        {
            for (EGoodsChangeType type : EGoodsChangeType.values()) {
                if (this.containsKey(type.getId())) {
                    Logger.getLogger("EGoodsTypeChange").error("EGoodsTypeChange ID:" + type.getId() + " is Duplicate !!");
                } else {
                    put(type.getId(), type);
                }
            }
        }
    };
    private final short id;
    private final String desc;

    EGoodsChangeType(int id, String desc) {
        this.id = (short) id;
        this.desc = desc;
    }

    public int getId() {
        return id;
    }

    public String getDesc() {
        return desc;
    }

    public static final EGoodsChangeType getChangeType(int id) {
        EGoodsChangeType type = valueMap.get(id);
        if (type == null)
            type = GM_ADD;
        return type;
    }

    public static final int OFFSET_INC = 10000;
    public static final int OFFSET_DESC = 20000;

    public boolean checkValue(int value) {
        if (this.id < OFFSET_INC) {
            return true;
        } else if (this.id < OFFSET_DESC) {
            return value >= 0;
        } else {
            return value <= 0;
        }
    }

